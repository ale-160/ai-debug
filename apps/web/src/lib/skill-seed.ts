/**
 * 生成内置技能列表（带 id / 时间戳）。
 * 用于首次启动时注入到用户的 skills 列表。
 */
export function createBuiltinSkills(): Skill[] {
  const now = Date.now();
  return BUILTIN_SKILLS.map((skill, index) => ({
    ...skill,
    id: `builtin-${now}-${index}`,
    createdAt: now + index,
    updatedAt: now + index,
  }));
}

/**
 * 判断用户的 skills 列表是否需要注入内置技能。
 * 规则：列表为空时注入（用户已删除所有技能后不再强制注入）。
 */
export function shouldInjectBuiltinSkills(existingSkills: Skill[]): boolean {
  return existingSkills.length === 0;
}
